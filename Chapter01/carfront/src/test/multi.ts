export const calMulti = (a: number, b: number): number => {
  return a * b;
}
export const calDiv = (a: number, b: number): number => {
  return a / b;
}
export const calAdd = (a: number, b: number): number => {
  return a + b;
}

export const calSub = (a: number, b: number): number => {
  return a - b;
}